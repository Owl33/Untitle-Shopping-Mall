function Section(Product) {
    for (let i = 0; i < Product.length; i++) {
        const product = document.querySelector(".productimg");
        const productDiv = document.createElement("div");
        const productLink = document.createElement("a");
        const productImg = document.createElement("img");
        const productName = document.createElement("p");
        const productPrice = document.createElement("p");

        productLink.appendChild(productImg);
        productDiv.appendChild(productLink);
        productDiv.appendChild(productName);
        productDiv.appendChild(productPrice);
        product.appendChild(productDiv);

        productLink.href = "../productdetail.html";
        productImg.src = `../../`+Product[i].src;
        productName.innerText =  Product[i].name;
        productPrice.innerText = Product[i].price;

        productLink.addEventListener('click',function(){
            localStorage.setItem('product', JSON.stringify(Product[i]));
        });
    }
}

function categoryNameEvent(categoryName){
    const categoryname = document.querySelector(".product>h1");
    categoryname.innerText = categoryName[0].category;
}

function categorySubNameEvent(subcategoryName){
    const categoryname = document.querySelector(".product>h1");
    categoryname.innerText = subcategoryName[0].subcategory;
}

